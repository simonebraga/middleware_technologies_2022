package it.polimi.middleware.spark.utils;

// Nothing to say, just two coordinates for horizontal and vertical position

public class Position {

    private Double x;
    private Double y;

    public Position() {
    }

    public void setX(Double x) {
        this.x = x;
    }

    public void setY(Double y) {
        this.y = y;
    }

    public Double getX() {
        return x;
    }

    public Double getY() {
        return y;
    }
}
